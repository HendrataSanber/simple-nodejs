// var
var name = 'masbro'; // string
var legs = 4; // number
var isFish = false; // boolean

console.log(name); // John
console.log(legs); // 25
console.log(isFish); // true

// let
let fruit = 'alpukat'; // string
let quantity = 3; // number
let isRipe = true; // boolean

console.log(fruit); // apple
console.log(quantity); // 5
console.log(isRipe); // false

// const
const pi = 3.14; // number
const color = 'red'; // string
const isCircle = true; // boolean

console.log(pi); // 3.14
console.log(color); // red
console.log(isCircle); // false

// Tipe data primitif
let string1 = 'hello'; // string
let string2 = 'world'; // string

console.log(string1 + ' ' + string2); // "hello world"

let number1 = 1; // number
let number2 = 2; // number

console.log(number1 + number2); // 30

let boolean1 = true; // boolean
let boolean2 = false; // boolean

console.log(boolean1 && boolean2); // false

let kosong = null;

console.log(kosong); // null

let belumInit;

console.log(belumInit); // Undefined

// Tipe data non-primitif
let array = [1, 2, 3]; // array
let object = { name: 'masbro', legs: 4 }; // object

console.log(array[1]); // 1
console.log(object.name); // "John"

// Operator aritmatika
let angka1 = 10;
let angka2 = 20;

console.log(angka1 + angka2); // 30
console.log(angka1 - angka2); // -10
console.log(angka1 * angka2); // 200
console.log(angka1 / angka2); // 0.5
console.log(angka1 % angka2); // 10

// Operator perbandingan
console.log(angka1 == angka2); // false
console.log(angka1 === angka2); // false
console.log(angka1 != angka2); // true
console.log(angka1 !== angka2); // true
console.log(angka1 > angka2); // false
console.log(angka1 >= angka2); // false
console.log(angka1 < angka2); // true
console.log(angka1 <= angka2); // true
